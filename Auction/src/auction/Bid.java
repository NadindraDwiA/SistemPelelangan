    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nama_penawar.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Nama belum diisi !");
            nama_penawar.requestFocus();
        } else if (alamat.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Alamat belum diisi !");
            alamat.requestFocus();
        } else if (harga_tawar.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Harga Tawar belum diisi !");
            harga_tawar.requestFocus();
        } else if (kode_lelang.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Maaf, Kode Lelang belum diisi !");
        }else{
            try {
                ab.setKode(Integer.parseInt(kode_lelang.getText()));
                ab.setNama(nama_penawar.getText());
                ab.setAlamat(alamat.getText());
                ab.setHargaTawar(Integer.parseInt(harga_tawar.getText()));
                ab.simpanData(ab.getKode(),ab.getNama(),ab.getAlamat(),ab.getHargaTawar());
                JOptionPane.showMessageDialog(null, "Data Berhasil tersimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                reset_text();
            } catch (Exception ex) {
                System.err.println("Parameter yang dimasukkan salah");
                System.err.println("\nException yang ditangkap adalah : "+ex);
            }
            dispose();
            User user = new User();
            user.setVisible(true);
        }     
    }//GEN-LAST:event_jButton1ActionPerformed

