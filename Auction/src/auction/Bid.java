    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nama_penawar.getText().isEmpty() || alamat.getText().isEmpty() || harga_tawar.getText().isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Enter The data");
        }else{
            try {
                String str = "INSERT INTO bids(nama, alamat, harga_tawar) VALUES (?,?,?)";
                PreparedStatement Add = con.prepareStatement(str);
                Add.setString(1, nama_penawar.getText());
                Add.setString(2, alamat.getText());
                Add.setInt(3, Integer.valueOf(harga_tawar.getText()));
                Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Barang dimasukkan");
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Bid.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

